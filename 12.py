c=[-10,20,-31]#масив
d=0
e=0
for a in range(len(c)):#цикл
    e=c[a]+e#сума
f=e//len(c)#середнє арефметичне
for a in range(len(c)):
    if c[a]>=f:#умова
        d+=1#
print(d)
