import numpy as np
import random
arr = np.zeros(10, dtype=int)
for i in range(len(arr)):#аповнення масиву рандомними числами від 0 до 10
    arr[i] = random.randint(0, 10)
print(f'Не відсортований  масив:{arr}\nВідсортований по зростанню масив:{np.sort(arr)}')#Сортуємо по зростанню масив за допомогою функції np.sort(arr)
