#Заданий масив А (1:20). Знайти добуток всіх його ненульових елементів.
#Виконав студент групи 122-Д Наральник Богдан
import numpy as np
import random
A = np.zeros(10, dtype=int)#заповнюємо масив нулями типу int
product = 1
for i in range(len(A)):
    A[i] = random.randint(0, 20)#заповнюємо масив рнадомними числами від 0 до 20
    if A[i] != 0:#знаходимо ненульові елементи та перемножаємо їх
        product *= A[i]
print(f'Масив елементів:{A}\nДобуток всіх ненульових елементів: {product}')
